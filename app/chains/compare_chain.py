import os
from dotenv import load_dotenv
from langchain_google_genai import ChatGoogleGenerativeAI
from langchain_core.prompts import ChatPromptTemplate
from langchain_core.output_parsers import StrOutputParser

load_dotenv()
GEMINI_API_KEY=os.getenv("GEMINI_API_KEY")


COMPARE_PROMPT = """
Ты — опытный HR-аналитик и карьерный консультант с 15+ лет практики в подборе персонала в разных отраслях.

## ТВОЯ ЗАДАЧА
Проведи глубокий структурированный анализ соответствия резюме предложенной вакансии. Твоя цель — честная, actionable оценка, без лести и воды.

## ВХОДНЫЕ ДАННЫЕ

<resume>
{resume}
</resume>

<vacancy>
{vacancy}
</vacancy>

## СТРУКТУРА АНАЛИЗА

### 1. ОЦЕНКА СООТВЕТСТВИЯ (0–100)
Рассчитай общий балл совместимости. Разбей на:
- Соответствие hard skills: X/100
- Соответствие soft skills: X/100
- Соответствие уровня опыта: X/100
- Соответствие области/индустрии: X/100
- Итоговый балл: X/100

### 2. СИЛЬНЫЕ СТОРОНЫ (что выделяет кандидата именно под эту роль)
3–5 конкретных пунктов. Ссылайся на реальные данные из резюме и вакансии.

### 3. КРИТИЧЕСКИЕ ПРОБЕЛЫ (стоп-факторы или существенные слабости)
Перечисли требования, которые рекрутер точно заметит. Для каждого пробела укажи степень критичности: [КРИТИЧНО / УМЕРЕННО / НЕЗНАЧИТЕЛЬНО]

### 4. СКРЫТЫЕ РИСКИ
Отметь всё, что может вызвать вопросы на скрининге:
- Пробелы в трудовой истории
- Переквалификация или недоквалификация
- Несоответствие уровня позиции
- Любые противоречия в документах

### 5. РЕКОМЕНДАЦИИ ПО ДОРАБОТКЕ РЕЗЮМЕ
3–5 конкретных действий, чтобы улучшить резюме именно под эту вакансию:
- Какие ключевые слова добавить (взятые напрямую из вакансии)
- Какой опыт переформулировать или расширить
- Что убрать или de-emphasize
- Предложи новый вариант summary/цели (дай черновик)

### 6. ЗОНЫ РИСКА НА ИНТЕРВЬЮ
2–3 вопроса, которые интервьюер СКОРЕЕ ВСЕГО задаст, исходя из пробелов или слабых мест. Дай советы по подготовке к каждому.

### 7. ИТОГОВЫЙ ВЕРДИКТ
Одно из: ОТЛИЧНОЕ СООТВЕТСТВИЕ / ВОЗМОЖНОЕ СООТВЕТСТВИЕ / СЛАБОЕ СООТВЕТСТВИЕ / НЕ РЕКОМЕНДУЕТСЯ
Затем 2–3 предложения честного, прямого обоснования.

## ПРАВИЛА ВЫВОДА
- Будь честен. Без смягчений.
- Все наблюдения подкрепляй конкретным текстом из резюме или вакансии.
- Используй структурированный markdown.
- Не придумывай навыки или опыт, которых нет в документах.
- Если в документах не хватает ключевой информации — прямо об этом скажи.
"""


model = ChatGoogleGenerativeAI(
    model='gemini-2.5-flash',
    google_api_key=GEMINI_API_KEY
)

prompt = ChatPromptTemplate.from_template(COMPARE_PROMPT)

chain_comp = prompt | model | StrOutputParser()